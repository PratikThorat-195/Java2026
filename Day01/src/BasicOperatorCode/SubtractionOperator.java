package BasicOperatorCode;

public class SubtractionOperator 
{
  public static void main(String[] args)
  {
	  int a = 10; 
	  int b = 05;
	  int c = a - b;
	  System.out.println("Subtraction of a - b = "+c); // answer will be 5
	  
	  int p = 10;
	  int q = 30;
	  int r = p - q;
	  System.out.println("Subtaction of p - q = "+r); // answer will be -20 
  }
}
